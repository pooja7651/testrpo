package hibernatecollection;
import java.util.*;
public class Students {
	
	int id;
	String name;
	List<String> record;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List<String> getRecord() {
		return record;
	}
	public void setRecord(List<String> record) {
		this.record = record;
	}
	

}
